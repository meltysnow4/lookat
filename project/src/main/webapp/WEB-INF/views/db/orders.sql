DROP SEQUENCE ORDERS_SEQ;
CREATE SEQUENCE ORDERS_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 9999999
NOCYCLE
NOCACHE;

DROP TABLE ORDERS;
CREATE TABLE ORDERS
(
	O_NO NUMBER PRIMARY KEY,                        -- 주문번호
	O_M_NO NUMBER NOT NULL,                         -- 회원번호
	O_C_NO NUMBER,                                  -- 쿠폰번호
	O_P_NO NUMBER NOT NULL,                         -- 상품번호
	O_AMOUNT NUMBER NOT NULL,                       -- 주문수량
	O_REGDATE DATE NOT NULL,                        -- 주문일자
	O_ADDRESS VARCHAR2(100) NOT NULL,               -- 수령자주소
	O_CELLPHONE VARCHAR2(2) NOT NULL,               -- 수령자연락처
	O_PAYMENT NUMBER NOT NULL,                      -- 결제방법
	O_NOTE VARCHAR2(4000),                          -- 기타메모
	O_PRICE NUMBER NOT NULL,                        -- 주문금액
	FOREIGN KEY (O_M_NO) REFERENCES MEMBERS (M_NO),
	FOREIGN KEY (O_P_NO) REFERENCES PRODUCTS (P_NO)
);

