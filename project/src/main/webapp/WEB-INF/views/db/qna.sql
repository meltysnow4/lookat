DROP SEQUENCE QNA_SEQ;
CREATE SEQUENCE QNA_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 9999999
NOCYCLE
NOCACHE;

DROP TABLE QNA;
CREATE TABLE QNA (                    	 	 -- QNA 테이블
	Q_NO NUMBER PRIMARY KEY,                 -- 문의번호
	Q_M_ID VARCHAR2(50),		  			 -- 작성자 ID
	Q_P_NO NUMBER,							 -- 제품번호
	Q_TITLE VARCHAR2(200) NOT NULL,          -- 문의제목
	Q_CONTENT VARCHAR2(1000) NOT NULL,       -- 문의내용
	Q_PW VARCHAR2(30) NOT NULL,               -- 비밀번호
	Q_REGDATE DATE,                          -- 작성일
	Q_HIT NUMBER,                            -- 조회수
	Q_REP CHAR(2),                           -- 답변 유무  (1,2)
	Q_SECRET CHAR(2),                         -- 비밀글 유무 (1,2)
	Q_GROUP NUMBER NOT NULL,                 -- 게시글 구분 
	Q_GROUPORD NUMBER NOT NULL,              -- 정렬순서
	Q_DEPTH NUMBER NOT NULL,                 -- 글의순번
	FOREIGN KEY (Q_M_ID) REFERENCES MEMBERS (M_ID),
	FOREIGN KEY (Q_P_NO) REFERENCES PRODUCTS (P_NO)
);

